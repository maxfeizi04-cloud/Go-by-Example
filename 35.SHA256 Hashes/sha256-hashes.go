package main

import (
	"crypto/sha256" // SHA-256 哈希算法，属于 crypto 标准库
	"fmt"
)

func main() {
	s := "sha256 this string"

	// ============================================================
	// 1. 创建 SHA-256 哈希计算器
	//    sha256.New() 返回一个 hash.Hash 接口
	//    内部初始化了 SHA-256 算法的状态，准备接收数据
	// ============================================================
	h := sha256.New()

	// ============================================================
	// 2. 写入待哈希的数据
	//    Write 接受 []byte，所以需要 string → []byte 转换
	//    可以多次调用 Write，数据会追加拼接后再一起哈希
	//    例如：h.Write([]byte("hello")); h.Write([]byte("world"))
	//          等价于 h.Write([]byte("helloworld"))
	// ============================================================
	h.Write([]byte(s))

	// ============================================================
	// 3. 计算最终哈希值
	//    Sum(nil) 追加计算结果到 nil 切片并返回
	//    参数作用：如果传入已有切片，会把哈希结果追加到该切片后面
	//              传 nil 表示返回一个全新的切片
	//    返回值：32 字节的 []byte（SHA-256 固定输出 256 位 = 32 字节）
	// ============================================================
	bs := h.Sum(nil)

	// ============================================================
	// 4. 输出哈希结果
	// ============================================================

	// 原始字节切片 → 十进制数字数组
	fmt.Println(bs)
	// [131 199 84 58 69 159 214 174 ... 共32个数字]

	// %x → 十六进制字符串（常用格式，每字节2位，共64个字符）
	fmt.Printf("%x\n", bs)
	// 83c7543a459fd6ae...  (64位十六进制字符串)
}

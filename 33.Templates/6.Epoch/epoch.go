package main

import (
	"fmt"
	"time"
)

func main() {

	// ============================================================
	// 1. 获取当前时间（人类可读格式）
	// ============================================================
	now := time.Now()
	fmt.Println(now)
	// 输出：2024-01-15 14:30:00.123456789 +0800 CST
	//              人类可读的日期时间格式

	// ============================================================
	// 2. 时间 → Unix 时间戳（从 1970-01-01 00:00:00 UTC 起的秒数）
	//    三种精度：秒 / 毫秒 / 纳秒
	// ============================================================

	// Unix() → 秒级时间戳（10位数字）
	fmt.Println(now.Unix())
	// 输出：1705312200

	// UnixMilli() → 毫秒级时间戳（13位数字）
	fmt.Println(now.UnixMilli())
	// 输出：1705312200123

	// UnixNano() → 纳秒级时间戳（19位数字）
	fmt.Println(now.UnixNano())
	// 输出：1705312200123456789

	// 精度关系图解：
	//
	//   Unix()      = 1705312200
	//   UnixMilli() = 1705312200123
	//                   ├─────┤├─┤
	//                   秒部分  毫秒
	//   UnixNano()  = 1705312200123456789
	//                   ├─────┤├───────┤
	//                   秒部分  纳秒部分

	// ============================================================
	// 3. Unix 时间戳 → time.Time（反向转换）
	//    time.Unix(sec, nsec)
	//      sec  → 秒部分
	//      nsec → 纳秒补充部分（用于补充秒以下的精度）
	// ============================================================

	// 用秒级时间戳还原（纳秒部分为 0，精度只到秒）
	fmt.Println(time.Unix(now.Unix(), 0))
	// 输出：2024-01-15 14:30:00 +0800 CST
	//                    ↑ 没有小数部分，精度只到秒

	// 用纳秒级时间戳还原
	// 秒=0 表示"没有额外的秒"，纳秒=全部纳秒时间戳
	fmt.Println(time.Unix(0, now.UnixNano()))
	// 输出：2024-01-15 14:30:00.123456789 +0800 CST
	//                    ↑ 保留纳秒精度
}

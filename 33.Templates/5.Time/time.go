package main

import (
	"fmt"
	"time"
)

func main() {
	// 简写：用 p 代替 fmt.Println，减少重复输入
	p := fmt.Println

	// ============================================================
	// 1. 获取当前时间
	// ============================================================
	now := time.Now()
	p(now) // 例：2024-01-15 14:30:00.123456789 +0800 CST
	//              日期        时间          纳秒       时区

	// ============================================================
	// 2. 创建指定时间
	//    time.Date(年, 月, 日, 时, 分, 秒, 纳秒, 时区)
	// ============================================================
	then := time.Date(
		2009,      // 年
		11,        // 月（也可用 time.November）
		17,        // 日
		20,        // 时（24小时制）
		34,        // 分
		58,        // 秒
		651387237, // 纳秒（0 ~ 999999999）
		time.UTC)  // 时区：UTC 世界标准时间
	p(then) // 2009-11-17 20:34:58.651387237 +0000 UTC

	// ============================================================
	// 3. 获取时间的各个组成部分
	// ============================================================
	p(then.Year())       // 2009      年
	p(then.Month())      // November  月（英文名称）
	p(then.Day())        // 17        日
	p(then.Hour())       // 20        时
	p(then.Minute())     // 34        分
	p(then.Second())     // 58        秒
	p(then.Nanosecond()) // 651387237 纳秒部分
	p(then.Location())   // UTC       所属时区

	// ============================================================
	// 4. 获取星期几
	// ============================================================
	p(then.Weekday()) // Tuesday（星期二）

	// ============================================================
	// 5. 时间比较
	//    Before → 是否在另一个时间之前
	//    After  → 是否在另一个时间之后
	//    Equal  → 是否相等
	// ============================================================
	p(then.Before(now)) // true  （2009年在现在之前）
	p(then.After(now))  // false （2009年不在现在之后）
	p(then.Equal(now))  // false （两个时间不相等）

	// ============================================================
	// 6. 计算两个时间的差值
	//    Sub 返回 time.Duration 类型
	// ============================================================
	diff := now.Sub(then)
	p(diff) // 例：129876h35m23.456789s（以时分秒格式显示）

	// Duration 可以转换为不同单位
	p(diff.Hours())       // 例：129876.590...  总小时数（含小数）
	p(diff.Minutes())     // 例：7792595.39...  总分钟数（含小数）
	p(diff.Seconds())     // 例：467555723.4... 总秒数（含小数）
	p(diff.Nanoseconds()) // 例：467555723456789000 总纳秒数（整数）

	// ============================================================
	// 7. 时间加减
	//    Add 接受 Duration 参数
	//    加正数 → 向未来推移
	//    加负数 → 向过去推移
	// ============================================================
	p(then.Add(diff))  // then + diff = now（2009 + 时间差 = 现在）
	p(then.Add(-diff)) // then - diff = 更早的时间（2009 之前）
}

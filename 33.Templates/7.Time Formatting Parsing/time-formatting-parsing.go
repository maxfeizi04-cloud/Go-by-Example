package main

import (
	"fmt"
	"time"
)

func main() {
	p := fmt.Println

	t := time.Now()

	// ============================================================
	// 1. 格式化输出：time.Time → 字符串
	//    使用 time.Format(模板) 方法
	//    Go 的独特设计：不用 YYYY-MM-dd 占位符，
	//    而是用一个【固定参考时间】作为格式模板
	// ============================================================

	// RFC3339 是 Go 预定义的标准时间格式常量
	// 对应的参考时间值为："2006-01-02T15:04:05Z07:00"
	p(t.Format(time.RFC3339))
	// 输出：2026-05-13T16:26:44+08:00

	// ============================================================
	// 2. 解析字符串：字符串 → time.Time
	//    使用 time.Parse(模板, 字符串)
	//    模板必须是 Go 的参考时间，字符串是待解析的实际时间
	// ============================================================
	t1, _ := time.Parse(time.RFC3339, "2012-11-01T22:08:41+00:00")
	p(t1)
	// 输出：2012-11-01 22:08:41 +0000 UTC

	// ============================================================
	// 3. 自定义格式：模板中的数字有特殊含义
	//    Go 参考时间的记忆方式：
	//    1月2日 3时4分5秒 2006年 -0700时区
	//    即：01/02 03:04:05 2006  (美式日期 01/02/2006 03:04:05 PM)
	// ============================================================

	// "3:04PM" → 只显示 12小时制时间 + AM/PM
	p(t.Format("3:04PM"))
	// 输出：4:26PM

	// "Mon Jan _2 15:04:05 2006" → 完整的默认格式
	p(t.Format("Mon Jan _2 15:04:05 2006"))
	// 输出：Wed May 13 16:26:44 2026

	// "2006-01-02T15:04:05.999999-07:00"
	//   999999 表示"尽可能多的小数位，去掉末尾的零"
	p(t.Format("2006-01-02T15:04:05.999999-07:00"))
	// 输出：2026-05-13T16:26:44.185871+08:00
	//                                    ↑ 自动省略末尾零

	// ============================================================
	// 4. 用自定义格式解析字符串
	//    模板和字符串必须格式一致
	// ============================================================
	form := "3 04 PM"                    // 模板：时 分 AM/PM
	t2, _ := time.Parse(form, "8 41 PM") // 待解析字符串
	p(t2)
	// 输出：0000-01-01 20:41:00 +0000 UTC
	//       ↑ 年月日未指定，默认为零值；8PM → 20时

	// ============================================================
	// 5. 用 Printf 手动拼接时间字符串
	//    不使用 Format，而是用 Sprintf 按格式提取各字段
	//    %02d 表示两位数，不足补零
	// ============================================================
	fmt.Printf("%d-%02d-%02dT%02d:%02d:%02d-00:00\n",
		t.Year(), t.Month(), t.Day(), // 年、月、日
		t.Hour(), t.Minute(), t.Second()) // 时、分、秒
	// 输出：2026-05-13T16:26:44-00:00

	// ============================================================
	// 6. 格式不匹配时的错误处理
	//    模板是完整格式，但输入字符串只有时间没有日期
	//    → 解析失败，返回错误
	// ============================================================
	_, err := time.Parse("Mon Jan _2 15:04:05 2006", "8:41PM")
	p(err)
	// 输出：parsing time "8:41PM" as "Mon Jan _2 15:04:05 2006":
	//        cannot parse "8:41PM" as "Mon"
}

package main

import (
	"bytes"
	"fmt"
	"regexp"
)

func main() {

	// ============================================================
	// 1. 快捷匹配：直接用正则表达式字符串判断是否匹配
	//    - 第一个参数：正则表达式
	//    - 第二个参数：待匹配的字符串
	//    - 返回：(bool, error)
	// ============================================================
	match, _ := regexp.MatchString("p([a-z]+)ch", "peach")
	//  "p([a-z]+)ch" 解读：
	//    p      → 字面量 'p'
	//    ([a-z]+) → 捕获组：一个或多个小写字母
	//    ch     → 字面量 'ch'
	//  整体匹配：p + 若干小写字母 + ch → "peach" ✓
	fmt.Println(match) // true

	// ============================================================
	// 2. 编译正则：多次使用时应先 Compile，避免重复编译
	//    - Compile 返回 (*Regexp, error)
	//    - 如果正则语法有误，会返回非 nil error
	// ============================================================
	r, _ := regexp.Compile("p([a-z]+)ch")

	// ============================================================
	// 3. MatchString：字符串中是否存在匹配
	// ============================================================
	fmt.Println(r.MatchString("peach")) // true

	// ============================================================
	// 4. FindString：返回第一个匹配的子串
	//    "peach punch" 中第一个匹配 → "peach"
	// ============================================================
	fmt.Println(r.FindString("peach punch")) // peach

	// ============================================================
	// 5. FindStringIndex：返回第一个匹配的 [起始位置, 结束位置)
	//    "peach" 在 "peach punch" 中的下标 → [0, 5]
	// ============================================================
	fmt.Println("idx:", r.FindStringIndex("peach punch"))
	// idx: [0 5]

	// ============================================================
	// 6. FindStringSubmatch：返回完整匹配 + 各捕获组的内容
	//    [0] 整体匹配："peach"
	//    [1] 第1个捕获组 ([a-z]+)："ea"
	// ============================================================
	fmt.Println(r.FindStringSubmatch("peach punch"))
	// [peach ea]

	// ============================================================
	// 7. FindStringSubmatchIndex：返回完整匹配 + 各捕获组的下标
	//    [0,5]  → 整体匹配 "peach" 在原串中的 [start, end)
	//    [1,3]  → 捕获组 "ea"    在原串中的 [start, end)
	// ============================================================
	fmt.Println(r.FindStringSubmatchIndex("peach punch"))
	// [0 5 1 3]

	// ============================================================
	// 8. FindAllString：返回所有匹配的子串（第二个参数为最大匹配数，-1 表示全部）
	// ============================================================
	fmt.Println(r.FindAllString("peach punch pinch", -1))
	// [peach punch pinch]

	// ============================================================
	// 9. FindAllStringSubmatchIndex：所有匹配的子串及捕获组下标
	//    每个元素是 [整体start, 整体end, 组1start, 组1end, ...]
	// ============================================================
	fmt.Println("all:", r.FindAllStringSubmatchIndex(
		"peach punch pinch", -1))
	// all: [[0 5 1 3] [6 11 7 10] [12 17 13 16]]

	// ============================================================
	// 10. FindAllString 限制匹配数量：2 表示只取前 2 个
	// ============================================================
	fmt.Println(r.FindAllString("peach punch pinch", 2))
	// [peach punch]

	// ============================================================
	// 11. Match：接受 []byte 参数（与 MatchString 功能相同，只是输入类型不同）
	// ============================================================
	fmt.Println(r.Match([]byte("peach"))) // true

	// ============================================================
	// 12. MustCompile：编译失败时直接 panic
	//     适合用在包级别变量或 init 函数中，确保正则一定合法
	// ============================================================
	r = regexp.MustCompile("p([a-z]+)ch")
	fmt.Println("regexp:", r)
	// regexp: p([a-z]+)ch

	// ============================================================
	// 13. ReplaceAllString：将所有匹配替换为指定字符串
	//     "a peach" 中的 "peach" → "<fruit>"
	// ============================================================
	fmt.Println(r.ReplaceAllString("a peach", "<fruit>"))
	// a <fruit>

	// ============================================================
	// 14. ReplaceAllFunc：将所有匹配传入回调函数，用返回值替换
	//     这里把匹配到的 []byte 转为大写
	// ============================================================
	in := []byte("a peach")
	out := r.ReplaceAllFunc(in, bytes.ToUpper)
	fmt.Println(string(out))
	// a PEACH
}

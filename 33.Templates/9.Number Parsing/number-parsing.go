package main

import (
	"fmt"
	"strconv" // 字符串与基本数据类型互转的标准库
)

func main() {

	// ============================================================
	// 1. 字符串 → 浮点数
	//    ParseFloat(s string, bitSize int) (float64, error)
	//      s       → 待转换的字符串
	//      bitSize → 精度：32 表示 float32，64 表示 float64
	//      返回值  → 始终返回 float64，bitSize 只影响精度检查
	// ============================================================
	f, _ := strconv.ParseFloat("1.234", 64)
	fmt.Println(f) // 1.234

	// ============================================================
	// 2. 字符串 → 有符号整数
	//    ParseInt(s string, base int, bitSize int) (int64, error)
	//      s       → 待转换的字符串
	//      base    → 进制：0=自动检测，2/8/10/16=指定进制
	//      bitSize → 位宽：8/16/32/64，决定返回值的范围
	//      返回值  → 始终返回 int64
	// ============================================================
	i, _ := strconv.ParseInt("123", 0, 64)
	//                        "123"  base=0 自动检测 → 十进制
	fmt.Println(i) // 123

	// ============================================================
	// 3. 十六进制字符串 → 整数
	//    字符串以 "0x" 开头 + base=0 → 自动识别为十六进制
	//    0x1c8 = 1×16² + 12×16¹ + 8×16⁰ = 256 + 192 + 8 = 456
	// ============================================================
	d, _ := strconv.ParseInt("0x1c8", 0, 64)
	//                        "0x1c8"  base=0 自动检测 → 十六进制
	fmt.Println(d) // 456

	// ============================================================
	// 4. 字符串 → 无符号整数
	//    ParseUint 与 ParseInt 类似，但不允许负数
	//    适合处理永远非负的值（如计数器、ID、端口号等）
	// ============================================================
	u, _ := strconv.ParseUint("789", 0, 64)
	fmt.Println(u) // 789

	// ============================================================
	// 5. Atoi：最常用的快捷方式
	//    Atoi = ASCII to Integer
	//    等价于 ParseInt(s, 10, 0)，始终按十进制解析
	//    返回 int（不是 int64）
	// ============================================================
	k, _ := strconv.Atoi("135")
	fmt.Println(k) // 135

	// ============================================================
	// 6. 转换失败时的错误处理
	//    "wat" 不是合法的十进制数字 → 返回错误
	//    第一个返回值为 0（零值）
	// ============================================================
	_, e := strconv.Atoi("wat")
	fmt.Println(e)
	// 输出：strconv.Atoi: parsing "wat": invalid syntax
}

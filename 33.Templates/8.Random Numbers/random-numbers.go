package main

import (
	"fmt"
	"math/rand/v2" // Go 1.22+ 的随机数包（v2 版本，取代旧的 math/rand）
)

func main() {

	// ============================================================
	// 1. 全局随机数：使用包级别的默认随机源
	//    每次运行程序，结果都不同
	// ============================================================

	// IntN(n) → 生成 [0, n) 范围的随机整数
	fmt.Print(rand.IntN(100), ",") // 例：42,
	fmt.Print(rand.IntN(100))      // 例：67
	fmt.Println()

	// Float64() → 生成 [0.0, 1.0) 范围的随机浮点数
	fmt.Println(rand.Float64()) // 例：0.6645600958544357

	// 生成 [5.0, 10.0) 范围的随机浮点数
	// 原理：[0,1) × 5 → [0,5) + 5 → [5,10)
	fmt.Print((rand.Float64()*5)+5, ",") // 例：7.234,
	fmt.Print((rand.Float64() * 5) + 5)  // 例：9.123
	fmt.Println()

	// ============================================================
	// 2. 可复现的随机数（伪随机）
	//    使用固定的种子（seed）创建独立的随机源
	//    相同种子 → 每次运行产生完全相同的序列
	//    用途：调试、测试、需要可复现结果的场景
	// ============================================================

	// NewPCG(seed_hi, seed_lo) → 创建一个 PCG 随机源
	// PCG = Permuted Congruential Generator（一种高质量伪随机算法）
	// 两个 uint64 参数组合成种子
	s2 := rand.NewPCG(42, 1024)

	// New(source) → 用指定的随机源创建独立的 Rand 实例
	// 这样 r2 的随机序列不会被全局随机函数干扰
	r2 := rand.New(s2)
	fmt.Print(r2.IntN(100), ",") // 每次运行结果固定：例 89,
	fmt.Print(r2.IntN(100))      // 每次运行结果固定：例 37
	fmt.Println()

	// ============================================================
	// 3. 验证：相同种子 → 相同结果
	//    用完全相同的种子 (42, 1024) 再创建一个实例
	// ============================================================
	s3 := rand.NewPCG(42, 1024) // 种子与 s2 完全相同
	r3 := rand.New(s3)
	fmt.Print(r3.IntN(100), ",") // 输出与 r2 完全相同
	fmt.Print(r3.IntN(100))      // 输出与 r2 完全相同
	fmt.Println()
}
